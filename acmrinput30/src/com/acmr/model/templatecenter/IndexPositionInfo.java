package com.acmr.model.templatecenter;

/**
 * 别名位置信息
 * @author caosl
 */
public class IndexPositionInfo {
	private String startRow;
	private String endRow;
	private String startCol;
	private String endCol;
	public String getStartRow() {
		return startRow;
	}
	public void setStartRow(String startRow) {
		this.startRow = startRow;
	}
	public String getEndRow() {
		return endRow;
	}
	public void setEndRow(String endRow) {
		this.endRow = endRow;
	}
	public String getStartCol() {
		return startCol;
	}
	public void setStartCol(String startCol) {
		this.startCol = startCol;
	}
	public String getEndCol() {
		return endCol;
	}
	public void setEndCol(String endCol) {
		this.endCol = endCol;
	}
}
